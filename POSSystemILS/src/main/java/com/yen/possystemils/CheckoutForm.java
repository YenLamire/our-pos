    //this method basically computes all the items in the listbox
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        for (int i = 0; i < jList1.getModel().getSize(); i++) { //loop all items inside listbox 
            String firstText = String.valueOf(jList1.getModel().getElementAt(i)); //gets the text of the currentIndex of listbox
            String[] keyNameArray = firstText.split("x"); //split it into 2 strings because x is our splitter
            Double itemPrice = Inventory.priceStorage.get(keyNameArray[0])/*get first string which is our item name*/ 
                    * Double.parseDouble(keyNameArray[1]); 
            //then get the second string which is our price which we will then parse to a double
            total += itemPrice; //add the prices to our total
            System.out.println(itemPrice); //for debugGGGGGGGGGGGG (NOT GOING TO BE REMOVED SIR PARA WHY NOT)
        }
        jLabel2.setText("Total Price: " + total); //set price
    }//GEN-LAST:event_jButton4ActionPerformed

