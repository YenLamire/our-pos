    //this method here gets the text (which is our itemName) and checks if 
    // "priceStorage" contains it, if it isn't it tells the user that the item is not in storage
    // if it is, then it will show up the price and the remaining stocks
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String textvalue = jTextField1.getText();
        if (!Inventory.priceStorage.containsKey(textvalue) || !Inventory.itemStocks.containsKey(textvalue)) {
            JOptionPane.showMessageDialog(this, "Item does not exist in storage", "ILS POS", JOptionPane.ERROR_MESSAGE);
            return;
        }
        jLabel3.setText("Price Check: " + Inventory.priceStorage.get(textvalue));
        jLabel4.setText("Stocks Left: " + Inventory.itemStocks.get(textvalue));

    }//GEN-LAST:event_jButton2ActionPerformed

