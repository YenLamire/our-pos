    // -> set 0 -> loop then get stocks val -> set stock val - 1 = less 1 then clear everythiing
    //nakakalito talaga loops sa jlist na to yoko na
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        total = 0.0;

        for (int i = 0; i < jList1.getModel().getSize(); i++) {
            int curStocks = (Inventory.itemStocks.get(jList1.getModel().getElementAt(i).split("x")[0]));
            Inventory.itemStocks.replace(jList1.getModel().getElementAt(i).split("x")[0], curStocks - 1);
        }
        checkoutItems.clear();
        jList1.setModel(checkoutItems);
    }//GEN-LAST:event_jButton5ActionPerformed

